package com.ecodation.primitive;

public class PrimitiveDataTypes {

	public static void main(String[] args) {
		//tam sayılar
		byte b1=125;
		short s1=155;
		int i1=250;
		long l1=125225156666L;
		
		System.out.println(b1+s1+i1+l1);
		
		//virgüllü sayılar
		float f2=44.55f;
		double d2=66.88;
		System.out.println(f2+d2);
		
		//char
		char c3='\u8526';
		System.out.println(c3);
		
		//karar
		boolean b4=true;
		System.out.println(b4);

	}

}
