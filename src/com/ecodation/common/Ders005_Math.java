package com.ecodation.common;

public class Ders005_Math {

	public static void main(String[] args) {
	
		System.out.println("E: "+Math.E);
		System.out.println("PI: "+Math.PI);
		System.out.println("Mutlak değer: "+Math.abs(-5));
		System.out.println("iki sayı en küçüğü: "+Math.min(2, 555));
		System.out.println("iki sayı en büyüğü:"+Math.max(9999, 2));
		System.out.println("Karekök: "+Math.sqrt(16));
		System.out.println("Üstlü sayı"+Math.pow(2, 5));
		
		System.out.println("Alta  yuvarla: "+Math.floor(2.9));
		System.out.println("Üste yuvarla: "+Math.ceil(2.1));
		System.out.println("Hem alta hem yukarı yuvarla: "+Math.round(6.4));
	
	}

}
