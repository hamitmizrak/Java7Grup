package com.ecodation.common;

import java.util.Scanner;

public class Ders018_Loop {

	public static void main(String[] args) {
		//kullanıcadan girdiği ayı ekranda göstersin.
	
		for(int i=0; i<=10;  i++) {
			System.out.print(i+" ");
		}
		System.out.println();
		for(int i=0; i<=10;  i=i+2) {
			System.out.print(i+" ");
		}
		System.out.println();
		for(int i=0; i<=10;  i=i+3) {
			System.out.print(i+" ");
		}
		System.out.println();
		for(int i=0; i<=10;  i=i+4) {
			System.out.print(i+" ");
		}
		System.out.println();
		for(int i=0; i<=10;  i=i+5) {
			System.out.print(i+" ");
		}
	}

}
